package com.americanexpress.merchant.dempgraphic.pojo;

public class MerchantInformationResponseData {

	private String se10;
	
	private String seName;
	
	private String seCategory;
	
	private String seZipCode;
	
	private String seLatlong;

	public String getSe10() {
		return se10;
	}

	public void setSe10(String se10) {
		this.se10 = se10;
	}

	public String getSeName() {
		return seName;
	}

	public void setSeName(String seName) {
		this.seName = seName;
	}

	public String getSeCategory() {
		return seCategory;
	}

	public void setSeCategory(String seCategory) {
		this.seCategory = seCategory;
	}

	public String getSeZipCode() {
		return seZipCode;
	}

	public void setSeZipCode(String seZipCode) {
		this.seZipCode = seZipCode;
	}

	public String getSeLatlong() {
		return seLatlong;
	}

	public void setSeLatlong(String seLatlong) {
		this.seLatlong = seLatlong;
	}
	
}
