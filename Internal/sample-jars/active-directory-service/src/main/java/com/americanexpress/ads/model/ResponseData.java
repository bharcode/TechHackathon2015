package com.americanexpress.ads.model;

public class ResponseData {

	private String adsAccountId;
	
	private String accountSince;
	
	private String accountStatus;
	
	private String accountRole;

	public String getAdsAccountId() {
		return adsAccountId;
	}

	public void setAdsAccountId(String adsAccountId) {
		this.adsAccountId = adsAccountId;
	}

	public String getAccountSince() {
		return accountSince;
	}

	public void setAccountSince(String accountSince) {
		this.accountSince = accountSince;
	}

	public String getAccountStatus() {
		return accountStatus;
	}

	public void setAccountStatus(String accountStatus) {
		this.accountStatus = accountStatus;
	}

	public String getAccountRole() {
		return accountRole;
	}

	public void setAccountRole(String accountRole) {
		this.accountRole = accountRole;
	}
	
	
}
